package tientt.exceptions;

/**
 *
 * @author natton
 */
public class SupplierForeignConstraintException extends Exception{

    public SupplierForeignConstraintException() {
    }

    public SupplierForeignConstraintException(String message) {
        super(message);
    }

    public SupplierForeignConstraintException(String message, Throwable cause) {
        super(message, cause);
    }

    public SupplierForeignConstraintException(Throwable cause) {
        super(cause);
    }

    public SupplierForeignConstraintException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

   
    
    
}
